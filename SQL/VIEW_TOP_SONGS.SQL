SELECT
    ROW_NUMBER() OVER (ORDER BY COUNT(1) DESC) AS Rank,
    INFO.G_TITLE || ' - ' || INFO.G_ARTIST AS TITLE_ARTIST,
    COUNT(1) AS TOTAL_PLAYS

FROM 
    LISTEN_HISTORY LH
        INNER JOIN MUSIC_INFO_GOOGLE_ONLY INFO ON
            INFO.SONG_URL = LH.SONG_URL

GROUP BY
    INFO.G_TITLE || ' - ' || INFO.G_ARTIST

ORDER BY COUNT(1) DESC

LIMIT 50;